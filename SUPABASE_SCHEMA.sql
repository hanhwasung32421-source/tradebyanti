create policy "allow_all_users" on public.users for all to anon using (true) with check (true);

drop policy if exists "allow_all_sessions" on public.sessions;
create policy "allow_all_sessions" on public.sessions for all to anon using (true) with check (true);

drop policy if exists "allow_all_balances" on public.balances;
create policy "allow_all_balances" on public.balances for all to anon using (true) with check (true);

drop policy if exists "allow_all_positions" on public.positions;
create policy "allow_all_positions" on public.positions for all to anon using (true) with check (true);

drop policy if exists "allow_all_trades" on public.trades;
create policy "allow_all_trades" on public.trades for all to anon using (true) with check (true);

